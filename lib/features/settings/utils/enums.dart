/// [SettingsType] is used to indicate the type of settings.
/// wether [normal], [financial] or [grave].
/// 
/// [SettingsType.normal] settings are used for normal settings.
/// They are usually green in the UI.
/// 
/// [SettingsType.financial] settings are used for financial or commercial settings
/// They are usually orange.
/// 
/// Then [SettingsType.grave] settings are used for hazardous actions
enum SettingsType{normal, financial, grave}